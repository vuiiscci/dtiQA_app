        Vector3D mu31Fitted = mu11Fitted.cross(mu21Fitted);
	Vector3D mu32Fitted = mu12Fitted.cross(mu22Fitted);


	assertEquals(1.0, Math.abs(mu11Fitted.dot(mu11)), 0.001);
	assertEquals(1.0, Math.abs(mu21Fitted.dot(mu21)), 0.001);
	
