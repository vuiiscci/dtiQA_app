	double k11 = -105.31894937364204;
	double k21 = -18.678910981501538;
	double k12 = -218.5863091878245;
	double k22 = -46.43943125843115;

	TwoFibreBinghamFitter fitter = new TwoFibreBinghamFitter(samples);

	Vector3D[] initAxes = new Vector3D[4];
	double[] initKs = new double[4];

 
	// 	// move them off by 10 degrees
	initAxes[0] = Rotations.rotateVector(mu11, mu21, Math.PI / 18.0);
