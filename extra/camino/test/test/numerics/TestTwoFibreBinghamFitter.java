	Vector3D mu22Fitted = new Vector3D(Math.cos(params[7])*Math.cos(params[8])*Math.cos(params[6]) - Math.sin(params[7])*Math.sin(params[8]), Math.cos(params[8])*Math.cos(params[6])*Math.sin(params[7]) + Math.cos(params[7])*Math.sin(params[8]), -(Math.cos(params[8])*Math.sin(params[6])));
	
	
